#include <stdio.h>
/**
 * op_add - Entry of the program
 * @a: First number
 * @b: Second number
 * Return: (a + b)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry of the program
 * @a: First number
 * @b: Second number
 * Return: (a - b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Entry of the program
 * @a: First number
 * @b: Second number
 * Return: (a * b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Entry of the program
 * @a: First number
 * @b: Second number
 * Return: (a / b)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry of the program
 * @a: First number
 * @b: Second number
 * Return: (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
